import time  # Импорт модуля time для работы со временем

def get_random_email() -> str:  # Объявление функции с аннотацией возвращаемого типа (str)
    return f"test.{time.time()}@example.com"  # Возвращает строку email

# Как это работает:
# time.time()
#
# Возвращает текущее время в секундах с начала Unix-эпохи (1 января 1970 года).
#
# Например: 1712345678.123456 (целая часть — секунды, дробная — микросекунды).
#
# Это гарантирует, что каждый вызов функции даёт уникальное значение.
#
# Форматирование email (f"test.{time.time()}@example.com")
#
# Подставляет текущее время в строку, создавая email вида:
#
# text
# test.1712345678.123456@example.com
# Префикс test. и домен @example.com — фиксированные части.